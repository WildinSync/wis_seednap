"""Self-contained HTML run report (opt-in, ``report.html_report``).

Renders a single portable ``.html`` file -- charts are matplotlib figures
embedded as base64 PNGs, so there are no external assets, no CDN, and no
JavaScript. Built from the read-tracking table plus a headline-summary dict.

matplotlib is an optional dependency: it is imported lazily, and if it is
missing the report still renders (table + summary + warnings) with a
``[WARN]`` instead of crashing (CLAUDE.md section 4).
"""

